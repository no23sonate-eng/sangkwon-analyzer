def match_franchise(store_name: str) -> dict:
    """상호명에서 프랜차이즈 브랜드를 매칭한다.

    Returns:
        {"brand": str, "업종": str, "연매출": int, "월매출": int} or empty dict
    """
    if not store_name:
        return {}

    name = store_name.strip().upper()

    # 1차: 정확 매칭
    for brand, info in FRANCHISE_DB.items():
        if brand.upper() in name or brand in store_name:
            return {
                "brand": brand,
                "업종": info["업종"],
                "연매출": info["연매출"],
                "월매출": info["연매출"] // 12,
                "가맹점수": info["가맹점수"],
            }

    # 2차: 별칭 매칭
    for alias, brand in _ALIASES.items():
        if alias.upper() in name:
            info = FRANCHISE_DB[brand]
            return {
                "brand": brand,
                "업종": info["업종"],
                "연매출": info["연매출"],
                "월매출": info["연매출"] // 12,
                "가맹점수": info["가맹점수"],
            }

    return {}


def match_franchises_in_df(store_df):
    """점포 DataFrame에서 프랜차이즈를 일괄 매칭한다.

    Returns:
        list[dict]: 매칭된 점포 목록
        [{"상호명": str, "brand": str, "업종": str, "월매출": int, ...}, ...]
    """
    import pandas as pd

    if store_df is None or store_df.empty:
        return []

    name_col = "상호명" if "상호명" in store_df.columns else None
    if not name_col:
        return []

    results = []
    seen_brands = {}

    for _, row in store_df.iterrows():
        store_name = str(row.get(name_col, ""))
        matched = match_franchise(store_name)
        if matched:
            address = row.get("도로명주소", row.get("지번주소", ""))
            entry = {
                "상호명": store_name,
                "brand": matched["brand"],
                "업종": matched["업종"],
                "추정_월매출": matched["월매출"],
                "추정_연매출": matched["연매출"],
                "전국_가맹점수": matched["가맹점수"],
                "주소": address,
            }
            results.append(entry)

            # 브랜드별 집계
            b = matched["brand"]
            if b not in seen_brands:
                seen_brands[b] = {"count": 0, "월매출": matched["월매출"], "업종": matched["업종"]}
            seen_brands[b]["count"] += 1

    return results


def franchise_summary(store_df):
    """프랜차이즈 매칭 요약 통계를 반환한다.

    Returns:
        {
            "matched_stores": list,      # 매칭된 개별 점포
            "brand_summary": list,       # 브랜드별 요약
            "total_matched": int,        # 매칭된 점포 수
            "total_stores": int,         # 전체 점포 수
            "franchise_ratio": float,    # 프랜차이즈 비율 (%)
            "estimated_total_monthly": int,  # 프랜차이즈 추정 월매출 합계 (만원)
        }
    """
    if store_df is None or store_df.empty:
        return {
            "matched_stores": [],
            "brand_summary": [],
            "total_matched": 0,
            "total_stores": 0,
            "franchise_ratio": 0,
            "estimated_total_monthly": 0,
        }

    matched = match_franchises_in_df(store_df)
    total = len(store_df)

    # 브랜드별 집계
    brand_map = {}
    for m in matched:
        b = m["brand"]
        if b not in brand_map:
            brand_map[b] = {
                "brand": b,
                "업종": m["업종"],
                "점포수": 0,
                "추정_월매출": m["추정_월매출"],
                "추정_연매출": m["추정_연매출"],
                "전국_가맹점수": m["전국_가맹점수"],
            }
        brand_map[b]["점포수"] += 1

    brand_summary = sorted(brand_map.values(), key=lambda x: x["추정_월매출"] * x["점포수"], reverse=True)

    estimated_total = sum(m["추정_월매출"] for m in matched)

    return {
        "matched_stores": matched,
        "brand_summary": brand_summary,
        "total_matched": len(matched),
        "total_stores": total,
        "franchise_ratio": round(len(matched) / total * 100, 1) if total > 0 else 0,
        "estimated_total_monthly": estimated_total,
    }
